function myFunction1() {
    var x = document.getElementById("question1description");
    var y = document.getElementById("minusIcon1");
    var z = document.getElementById("plusIcon1");
    var display = window.getComputedStyle(x).display;
    if (display === "none") {
        x.style.display = "block";
        y.style.display = "block";
        z.style.display = "none";
    } else {
        x.style.display = "none";
        y.style.display = "none";
        z.style.display = "block";
    }
}

